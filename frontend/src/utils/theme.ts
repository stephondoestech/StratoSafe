import { createTheme, ThemeOptions } from '@mui/material/styles';

// Common theme settings shared between light and dark mode
const getBaseThemeOptions = (): ThemeOptions => ({
  typography: {
    fontFamily: '"Roboto", "Helvetica", "Arial", sans-serif',
    h1: {
      fontSize: '2.5rem',
      fontWeight: 500,
    },
    h2: {
      fontSize: '2rem',
      fontWeight: 500,
    },
    h3: {
      fontSize: '1.8rem',
      fontWeight: 500,
    },
  },
  components: {
    MuiButton: {
      styleOverrides: {
        root: {
          borderRadius: 4,
          textTransform: 'none',
        },
      },
    },
    MuiPaper: {
      styleOverrides: {
        root: {
          borderRadius: 8,
        },
      },
    },
  },
});

// Light mode palette
const lightPalette: ThemeOptions = {
  palette: {
    mode: 'light',
    primary: {
      main: '#1976d2',
    },
    secondary: {
      main: '#dc004e',
    },
    background: {
      default: '#f5f5f5',
      paper: '#ffffff',
    },
    text: {
      primary: 'rgba(0, 0, 0, 0.87)',
      secondary: 'rgba(0, 0, 0, 0.6)',
    },
  },
};

// Dark mode palette
const darkPalette: ThemeOptions = {
  palette: {
    mode: 'dark',
    primary: {
      main: '#90caf9',
    },
    secondary: {
      main: '#f48fb1',
    },
    background: {
      default: '#121212',
      paper: '#1e1e1e',
    },
    text: {
      primary: '#ffffff',
      secondary: 'rgba(255, 255, 255, 0.7)',
    },
  },
};

// Create theme based on mode
export const createAppTheme = (mode: 'light' | 'dark') => {
  const baseOptions = getBaseThemeOptions();
  const palette = mode === 'light' ? lightPalette : darkPalette;
  
  return createTheme({
    ...baseOptions,
    ...palette,
  });
};

// Default theme (for backward compatibility)
const theme = createAppTheme('light');

export default theme;
